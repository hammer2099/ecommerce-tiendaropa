    private void cargarCatalogoVisual(){
     if(catalogo==null) return;//evita error con la lista 
    //limpiamos la tabla
    modeloCatalogo.setRowCount(0);  
    //validacion de inventario
    for(Producto p: catalogo){    
    if (p.getstock()>0) { 
    modeloCatalogo.addRow(new Object[]{
    p.getId(), 
    p.getnombre(), 
    p.getprecio(), 
    p.getstock(), 
    p.getClass().getSimpleName()
    
            });
          }
        }
    }
