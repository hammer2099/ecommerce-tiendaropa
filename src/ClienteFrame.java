    private void btnAgregarCarritoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarCarritoActionPerformed
        /*.getSelectedRow();
        sirve para obtener el índice (número) de la fila
        que el usuario ha seleccionado actualmente.
        */
        int filaSeleccionada=tablaCatalogo.getSelectedRow();
        if(filaSeleccionada>=0){
            //Obtencion de datos de la tabla
            String idTabla=tablaCatalogo.getValueAt(filaSeleccionada, 0).toString();
            for(Producto p: catalogo){
                if(String.valueOf(p.getId()).equals(idTabla)){
                   carritoCompras.add(p);
                   //Extraccion de datos de "p"
                   String nombre=p.getnombre();
                   double precio=p.getprecio();
                   //Agregamos esto a la tabla visual del carrito
                   modeloCarrito.addRow(new Object[]{nombre, precio});
                   //sumamos al total de la compra el precio de los productos
                   totalCompra+=precio;
                   //mostramos el total de la compra
                   txtTotal.setText("Total: $" + totalCompra);
                   JOptionPane.showMessageDialog(this, p.getnombre() + "Agregado al carrito");
                   break;
                   }
                }
                //hace que el usuario seleccione primero un producto al carrito antes de agregarlo
                }else{
            JOptionPane.showMessageDialog(this,"Por favor, selecciona un producto del catalogo primero");
        }

    }//GEN-LAST:event_btnAgregarCarritoActionPerformed

    private void btnComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComprarActionPerformed
        /*.getRowCount():
         sirve para obtener el número total de filas (registros) presentes en un objeto de datos,
        como una tabla, un conjunto de resultados (ResultSet) o un modelo de tabla
        */
        if(modeloCarrito.getRowCount()==0){
            JOptionPane.showMessageDialog(this, "El carrito esta vacio");
            return;
        }
        String metodo =cbMetodoPago.getSelectedItem().toString();
        /*
        Inicializamos la variable pago para usarla
        */
        Pago miPago=null;
        if(metodo.equals("Efectivo")){
            miPago=new pagoEfectivo(totalCompra, totalCompra);
        }else if(metodo.equals("Tarjeta")){
            miPago = new pagoTarjeta(totalCompra, "0000-0000-0000-0000", "Visa");
        }else{
            miPago = new pagoTransferencia(totalCompra, "REF-9999");
        }
        miPago.procesarPago();
        generarFacturaTexto();
        JOptionPane.showMessageDialog(this, "¡Compra realizada con éxito por $" + totalCompra + " usando " + metodo + "!");
        /*
        Limpiamos el carrito para una nueva compra
        */
        modeloCarrito.setRowCount(0);
        totalCompra = 0.0;
        txtTotal.setText("Total: $0.0");
    }//GEN-LAST:event_btnComprarActionPerformed
        private void generarFacturaTexto(){
        /*PrintWritter:
            See utiliza para escribir texto formateado, incluyendo tipos primitivos (int, float, 
            boolean), cadenas (String) y objetos, de manera sencilla en archivos, consolas o flujos de red
            */
        try (PrintWriter writer = new PrintWriter(new FileWriter("Factura_Compra.txt"))) {
            writer.println("======= FACTURA DE VENTA =======");
            writer.println("Fecha: " + new java.util.Date());
            writer.println("--------------------------------");
            for (int i = 0; i < modeloCarrito.getRowCount(); i++) {
            writer.println(modeloCarrito.getValueAt(i, 0) + " - $" + modeloCarrito.getValueAt(i, 1));
            }
        writer.println("--------------------------------");
        writer.println("TOTAL PAGADO: $" + totalCompra);
        writer.println("Metodo: " + cbMetodoPago.getSelectedItem());
        writer.println("================================");
        JOptionPane.showMessageDialog(this, "Factura generada como 'Factura_Compra.txt'");
        }catch (IOException e) {
        System.out.println("Error al crear factura: " + e.getMessage());
        }
    }
