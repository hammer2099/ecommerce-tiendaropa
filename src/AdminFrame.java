    private void cbTipoProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbTipoProductoActionPerformed
       /*
        Creacion del evento del boton comboBox->Boton que tiene desplazamiento
        */
        String seleccionado= cbTipoProducto.getSelectedItem().toString();
        if(seleccionado.equals("Ropa")){
            txtDatoEspecial.setText("Talla:");
        }else if(seleccionado.equals("Talla")){
            txtDatoEspecial.setText("Material:");
        }else if(seleccionado.equals("Material")){
            txtDatoEspecial.setText("Unidad:");
        }
    }//GEN-LAST:event_cbTipoProductoActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
      /*SelectedRow():
        sirve para obtener el índice (número)
         de la fila que el usuario ha seleccionado
        actualmente. Devuelve un valor entero*/
        int filaSeleccionada=tablaInventario.getSelectedRow();
        if(filaSeleccionada>=0){
            int confirmar =JOptionPane.showConfirmDialog(this,"¿Estas seguro de eliminar este producto?");
            if(confirmar==JOptionPane.YES_OPTION){
                /*removeRow():
                es un método utilizado en programación para eliminar una 
                fila específica de una tabla, conjunto de datos (dataset) o cuadrícula (grid)
                Eliminamos de la lista el producto seleccionado
                , guardamos la lista actual de productos y mostramos el mensaje de eliminacion
                
                */
                listaProductos.remove(filaSeleccionada);
                gestor.guardarProductos(listaProductos);
                JOptionPane.showMessageDialog(this,"Producto Eliminado");
            }
        }else{
            JOptionPane.showMessageDialog(this,"Por favor, selecciona una fila de la tabla");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void tablaInventarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaInventarioMouseClicked
        int fila=tablaInventario.getSelectedRow();
        if(fila>=0){
            txtId.setText(modelo.getValueAt(fila, 0).toString());
            txtNombre.setText(modelo.getValueAt(fila,1).toString());
            txtPrecio.setText(modelo.getValueAt(fila, 2).toString());
            txtStock.setText(modelo.getValueAt(fila, 3).toString());
            cbTipoProducto.setSelectedItem(modelo.getValueAt(fila, 4).toString());
        }
    }//GEN-LAST:event_tablaInventarioMouseClicked

