    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
    /*
      Agregamos a la lista los datos ya creados   
     */
    gestor.guardarProductos(listaProductos);
    actualizarTabla();
    try{
        //Captura de datos de los botones:
        int id= Integer.parseInt(txtId.getText());
        String nombre= txtNombre.getText();
        double precio= Double.parseDouble(txtPrecio.getText());
        int stock= Integer.parseInt(txtStock.getText());
        String tipo =cbTipoProducto.getSelectedItem().toString();
        String especial = txtDatoEspecial.getText();
        //variable temporal para aplicar poliformismo
        Producto nuevo = null;
        //Creacion del objeto
        if(tipo.equals("Ropa")){
            Ropa nuevaRopa=new Ropa(id, nombre, precio, stock ,especial, "Unisex");
            /*Agregacion a la lista global
            El método addRow() es usado en DefaultTableModel
            de Swing, agrega una nueva fila de datos al final de un JTable
            */
            nuevo = new Ropa(id, nombre, precio, stock, especial, "Unisex");    
        }else if(tipo.equals("Tela")){
            nuevo = new Tela(id, nombre, precio, stock, especial, "Color Base");
              
        } if(nuevo!=null){
            /*
            Si la variable temporal no esta vacia agregamos los productos a la lista
            */
           listaProductos.add(nuevo); // 1. Agregamos a la lista
           gestor.guardarProductos(listaProductos); // 2. Guardamos 
           actualizarTabla(); // 3. Refrescamos la tabla
           JOptionPane.showMessageDialog(this, "Producto Agregado Exitosamente");
           limpiarCampos();     
                
            }
        }/*
            Excepcion para avisar que el precio,ID y Stock sean numeros
         */
        catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Error: Revisa que ID, Precio y Stock sean números");
        }
    }//GEN-LAST:event_btnAgregarActionPerformed
